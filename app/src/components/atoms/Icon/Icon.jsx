export default function Icon({
  name = 'search',
  size = 'medium',
  color = 'currentColor',
  className,
  ariaLabel
}) {
  const iconContent = icons[name]

  if (!iconContent) {
    logger.warn(`Icon "${name}" not found. Available icons: ${Object.keys(icons).join(', ')}`)
    return null
  }

  // Resolve size to pixels
  const pixelSize = typeof size === 'number' ? size : SIZE_MAP[size] || SIZE_MAP.medium

  const combinedClassName = [
    styles.icon,
    className
  ].filter(Boolean).join(' ')

  // Build color style
  const colorStyle = color !== 'currentColor' && color.startsWith('--')
    ? { color: `var(${color})` }
    : color !== 'currentColor'
    ? { color }
    : {}

  // Apply size to wrapper span
  const wrapperStyle = {
    ...colorStyle,
    width: `${pixelSize}px`,
    height: `${pixelSize}px`
  }

  // Clone SVG with explicit size attributes for bulletproof sizing
  const sizedIcon = cloneElement(iconContent, {
    width: pixelSize,
    height: pixelSize,
    style: { width: `${pixelSize}px`, height: `${pixelSize}px`, display: 'block' }
  })

  return (
    <span
      className={combinedClassName}
      style={wrapperStyle}
      aria-label={ariaLabel}
      aria-hidden={!ariaLabel}
      role={ariaLabel ? 'img' : undefined}
    >
      {sizedIcon}
    </span>
  )
}
