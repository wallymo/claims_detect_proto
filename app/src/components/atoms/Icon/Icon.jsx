export default function Icon({
  name = 'search',
  size = 'medium',
  color = 'currentColor',
  className,
  ariaLabel
}) {
  const iconContent = icons[name]

  if (!iconContent) {
    console.warn(`Icon "${name}" not found. Available icons: ${Object.keys(icons).join(', ')}`)
    return null
  }

  // Handle numeric sizes vs string size classes
  const isNumericSize = typeof size === 'number'
  const sizeClass = isNumericSize ? null : styles[size]

  const combinedClassName = [
    styles.icon,
    sizeClass,
    className
  ].filter(Boolean).join(' ')

  const baseStyle = color !== 'currentColor' && color.startsWith('--')
    ? { color: `var(${color})` }
    : color !== 'currentColor'
    ? { color }
    : {}

  const style = isNumericSize
    ? { ...baseStyle, width: size, height: size }
    : baseStyle

  return (
    <span
      className={combinedClassName}
      style={Object.keys(style).length ? style : undefined}
      aria-label={ariaLabel}
      aria-hidden={!ariaLabel}
      role={ariaLabel ? 'img' : undefined}
    >
      {iconContent}
    </span>
  )
}
